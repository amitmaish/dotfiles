local digraph = vim.fn.digraph_set

digraph("uh", "ʌ") -- stressed 'uh'
digraph("uu", "ə") -- schwa
digraph("ah", "ɔ") -- open o
digraph("aa", "ɑ") -- script a
digraph("ih", "ɪ") -- small capital I
digraph("ns", "̯") -- non-sylabic diacritic
digraph("na", "̃") -- nasal diacritic
digraph("um", "̈") -- umlaut
digraph("ww", "ʍ") -- "upside down capital W"
digraph("eh", "ε") -- epsilon
